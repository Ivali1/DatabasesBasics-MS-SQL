CREATE DATABASE Hotel
Use Hotel

CREATE TABLE Employees(
Id INT PRIMARY KEY IDENTITY,
FirstName NVARCHAR(50) NOT NULL,
LastName NVARCHAR(50) NOT NULL,
Title NVARCHAR(50) NOT NULL,
Notes NVARCHAR(MAX)
)
CREATE TABLE Customers(
AcountNumber INT PRIMARY KEY IDENTITY ,
FirstName NVARCHAR(50) NOT NULL,
LastName NVARCHAR(50) NOT NULL,
PhoneNumber INT NOT NULL,
EmergencyName NVARCHAR(50) NOT NULL,
EmergencyNumber INT NOT NULL,
Notes NVARCHAR(MAX)
)
CREATE TABLE RoomStatus(
RoomStatus NVARCHAR(50) PRIMARY KEY NOT NULL,
Notes NVARCHAR(MAX),
)

CREATE TABLE RoomTypes(
RoomType NVARCHAR(50) PRIMARY KEY NOT NULL,
Notes NVARCHAR(MAX),
)

CREATE TABLE BedTypes(
BedType NVARCHAR(50) PRIMARY KEY NOT NULL,
Notes NVARCHAR(MAX),
)

CREATE TABLE Rooms(
RoomNumber INT PRIMARY KEY,
RoomType NVARCHAR(50) NOT NULL,
BedType NVARCHAR(50) NOT NULL,
Rate NUMERIC(10,2) NOT NULL,
RoomStatus NVARCHAR(50),
Notes NVARCHAR(MAX)
)

CREATE TABLE Payments(
Id INT PRIMARY KEY IDENTITY,
EmployeeId INT,
PaymentDate DATE NOT NULL,
AcountNumber INT ,
FirstDateOccupied DATE NOT NULL,
LastDateOccupied DATE NOT NULL,
TotalDays INT NOT NULL,
AmountCharged NUMERIC(10,2) NOT NULL,
TaxRate NUMERIC(10,2)NOT NULL,
TaxAmout NUMERIC(10,2) NOT NULL,
PaymentTotal NUMERIC(10,2) NOT NULL,
Notes NVARCHAR(MAX)
)
CREATE TABLE Occupancies(
Id INT PRIMARY KEY IDENTITY,
EmployeeId INT ,
DateOccupied DATE NOT NULL,
AcountNumber INT,
RoomNumber INT,
RateAplieed NVARCHAR(50),
PhoneCharge NUMERIC(10,2),
Notes NVARCHAR(MAX)
)
